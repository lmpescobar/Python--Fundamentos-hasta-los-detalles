# Operaciones matemáticas básicas en Python

# Suma
print("Suma:")
print(2 + 2)  # Resultado: 4

# Resta
print("\nResta:")
print(4 - 2)  # Resultado: 2

# Multiplicación
print("\nMultiplicación:")
print(4 * 2)  # Resultado: 8

# División
print("\nDivisión:")
print(2 / 4)  # Resultado: 0.5

# Otras operaciones

# Potencia
print("\nPotencia (2 ** 3):")
print(2 ** 3)  # Resultado: 8

# División sin decimal (división entera)
print("\nDivisión sin decimal (5 // 4):")
print(5 // 4)  # Resultado: 1

# Módulo (residuo de la división)
print("\nMódulo (6 % 4):")
print(6 % 4)  # Resultado: 2

# Redondeo
print("\nRedondeo:")
print("round(3.1) =", round(3.1))  # Resultado: 3
print("round(3.9) =", round(3.9))  # Resultado: 4

# Valor absoluto
print("\nValor absoluto:")
print("abs(-20) =", abs(-20))  # Resultado: 20
print("abs(20) =", abs(20))    # Resultado: 20
