# Tipos de datos

# Strings o cadenas de texto
nombre = "Ricardo"
apellido = "Cuellar"
saludo = "Hola Mundo"

# Ints o enteros
numero = 2000
edad = 29
gatos = 5

# Floats o decimales
decimal = 20.42
temperatura = 27.3

# Booleanos
booleano = True
booleano_falso = False
is_gamer = True  # ejemplo adicional

# Listas (arrays)
lista = [1, 2, 3]
lista_abc = ["a", "b", "c"]

# Tupla (similar a lista pero inmutable)
tupla = (1, 2, 3)  # ejemplo básico

# Set
set_variable = {1, 2, 3}

# Diccionarios
diccionario = {
    "hello": "hola",
    "bye": "adios"
}

informacion_personal = {
    "name": "Ricardo",
    "age": 29,
    "cats": 5
}

